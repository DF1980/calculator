public class concatenationTwo {
    public static void main(String[] args) {
        String stringOne = "Gosia ma";
        String stringTwo = "psa";
        String stringThree = "3 koty";
        String stringFour = "brata o imieniu Stefan";
        System.out.println(stringOne + stringTwo);
        System.out.println(stringOne + stringThree);
        System.out.println(stringOne + stringFour);

        String stringOne1 = "Gosia ma";
        String stringTwo2 = "psa";
        String stringThree3 = "3 koty";
        String stringFour4 = "brata o imieniu Stefan";
        String space = " ";
        System.out.println(stringOne1 + space + stringTwo2);
        System.out.println(stringOne1 + space + stringThree3);
        System.out.println(stringOne1 + space + stringFour4);
    }
}
