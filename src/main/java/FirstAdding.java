public class FirstAdding {
    public static void main(String[] args) {
        int numberOne = 30;
        int numberTwo = 50;
        int result = numberOne + numberTwo;
        System.out.println(result);

        int numberThree = 30;
        double numberFour = 5.5;
        double result1 = numberThree - numberFour;
        System.out.println(result1);

        int numberFive = 25;
        int numberSix = 6;
        int modulo = numberFive % numberSix;
        System.out.println(modulo);
    }
}
