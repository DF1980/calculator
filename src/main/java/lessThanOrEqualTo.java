public class lessThanOrEqualTo {
    public static void main(String[] args) {
        int age = 19;
        if (age <= 10) {
            System.out.println("Janek na pewno ma mniej lub dokŁadnie 10 lat");
        }
        if (age > 10) {
            System.out.println(" Janek ma więcej niż 10 lat");
        }
    }
}
